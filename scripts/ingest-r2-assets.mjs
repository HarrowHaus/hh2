#!/usr/bin/env node
/*
 * ingest-r2-assets.mjs — upload a local folder of large runtime assets to a
 * Cloudflare R2 bucket and emit a manifest the app reads at runtime. This is the
 * same self-hosted-R2 model as the music catalog (scripts/ingest-music.mjs):
 * heavy assets live on R2 (free egress, immutable cache), git stays lean, and
 * the apps fetch-on-first-launch then rely on the browser cache.
 *
 * Used for the two Tier-C apps whose data is too big to commit:
 *   • Quake III  — the OpenArena FREE data (.pk3) + the wasm engine
 *   • BoxedWine  — the wasm engine + the LGPL Wine environment (free software)
 *
 * The OWNER runs this locally (wrangler + R2 creds required — NOT available in
 * the build sandbox). No proprietary content is ever uploaded: point --src at a
 * folder that contains only free/licensed files (OpenArena data, Wine, engines).
 *
 * Setup (once):
 *   wrangler r2 bucket create hh2-assets
 *   # make it public (r2.dev or a custom domain); note the public base URL
 *
 * Run (examples):
 *   node scripts/ingest-r2-assets.mjs \
 *     --src ./_assets/quake3 --prefix quake3 \
 *     --bucket hh2-assets --public-base https://assets.example.com \
 *     --manifest data/quake3-manifest.json
 *
 *   node scripts/ingest-r2-assets.mjs \
 *     --src ./_assets/boxedwine --prefix boxedwine \
 *     --bucket hh2-assets --public-base https://assets.example.com \
 *     --manifest data/boxedwine-manifest.json
 *
 * Objects are uploaded with: Cache-Control: public, max-age=31536000, immutable
 */

import { promises as fs } from 'node:fs'
import path from 'node:path'
import { execFile } from 'node:child_process'
import { promisify } from 'node:util'

const run = promisify(execFile)
const CACHE_CONTROL = 'public, max-age=31536000, immutable'
const CONTENT_TYPE = {
  '.js': 'text/javascript', '.mjs': 'text/javascript', '.wasm': 'application/wasm',
  '.json': 'application/json', '.html': 'text/html', '.css': 'text/css',
  '.pk3': 'application/octet-stream', '.zip': 'application/zip',
  '.data': 'application/octet-stream', '.mem': 'application/octet-stream',
  '.png': 'image/png', '.jpg': 'image/jpeg', '.wad': 'application/octet-stream',
}

function parseArgs(argv) {
  const a = {}
  for (let i = 2; i < argv.length; i++) {
    const k = argv[i]
    if (k === '--dry-run') a.dryRun = true
    else if (k.startsWith('--')) a[k.slice(2)] = argv[++i]
  }
  return a
}

async function* walk(dir) {
  for (const e of await fs.readdir(dir, { withFileTypes: true })) {
    const full = path.join(dir, e.name)
    if (e.isDirectory()) yield* walk(full)
    else yield full
  }
}

async function r2put(bucket, key, file, dryRun) {
  const ct = CONTENT_TYPE[path.extname(file).toLowerCase()] || 'application/octet-stream'
  if (dryRun) return ct
  await run('npx', [
    'wrangler', 'r2', 'object', 'put', `${bucket}/${key}`,
    '--file', file, '--content-type', ct, '--cache-control', CACHE_CONTROL, '--remote',
  ])
  return ct
}

async function main() {
  const args = parseArgs(process.argv)
  if (!args.src || !args.bucket || !args['public-base'] || !args.prefix || !args.manifest) {
    console.error('usage: node scripts/ingest-r2-assets.mjs --src <dir> --prefix <keyPrefix> --bucket <r2bucket> --public-base <url> --manifest <out.json> [--dry-run]')
    process.exit(1)
  }
  const srcRoot = path.resolve(args.src)
  const publicBase = args['public-base'].replace(/\/$/, '')
  const files = []

  for await (const file of walk(srcRoot)) {
    const rel = path.relative(srcRoot, file).split(path.sep).join('/')
    const key = `${args.prefix}/${rel}`
    const ct = await r2put(args.bucket, key, file, args.dryRun)
    const { size } = await fs.stat(file)
    files.push({ path: rel, key, url: `${publicBase}/${key}`, contentType: ct, size })
    console.log(`${args.dryRun ? '[dry-run] ' : ''}${key}  (${(size / 1048576).toFixed(2)}MB)`)
  }

  files.sort((a, b) => a.path.localeCompare(b.path))
  const manifest = {
    _comment: 'GENERATED by scripts/ingest-r2-assets.mjs — do not hand-edit.',
    generatedAt: new Date().toISOString(),
    r2PublicBase: publicBase,
    prefix: args.prefix,
    files,
  }
  await fs.writeFile(path.resolve(args.manifest), JSON.stringify(manifest, null, 2) + '\n')
  console.log(`\n${args.dryRun ? '[dry-run] ' : ''}wrote ${files.length} files -> ${args.manifest}`)
}

main().catch((e) => { console.error(e); process.exit(1) })
